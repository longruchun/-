package com.shaodq.chapter2.d20180723;

import java.util.Arrays;

public class teachSample3 {

	public static void main(String[] args) {
		char[] chars={'A','b','C',' ','@','^','#','G','a'};
		
		char MAX=chars[0];
		
		for(int i=0;i<chars.length;i++){
			
				if(chars[i]>MAX){
					MAX=chars[i];
				}
			
		}
		
		System.out.println("数组中的最大值是:"+MAX);
	}

}
